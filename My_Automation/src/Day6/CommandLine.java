package Day6;

public class CommandLine 
{

	//String[] Prince...............Array of string
	//Command Line Argument
	//Yes we can overload the main method
	public static void main(String[] Prince) 
	{		
		System.out.println("Print the output of Command line input: "+Prince.length);
		System.out.println("Print the value on Index 0: "+Prince[0]);
		System.out.println("Print the value on Index 1: "+Prince[1]);
		System.out.println("Print the value on Index 2: "+Prince[2]);
		System.out.println("Print the value on Index 3: "+Prince[3]);
		System.out.println("Print the value on Index 4: "+Prince[4]);
		
	}
}
