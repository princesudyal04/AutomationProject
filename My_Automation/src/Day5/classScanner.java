package Day5;

import java.util.Scanner;

public class classScanner 
{

	public static void main(String[] args) 
	{
		System.out.println("Give the input of integer a");
		Scanner s=new Scanner(System.in);
		int a=s.nextInt();
		System.out.println("Give the value of float f");
		float b=s.nextFloat();
		System.out.println("The value of integer a= "+a);
		System.out.println("The value of float b= "+b);
		
	}
}
